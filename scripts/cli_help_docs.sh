# Usage:
#   ./cli_help_docs.sh generate  - Generate CLI help documentation
#   ./cli_help_docs.sh check     - Check if documentation is up to date

repo_root_dir="$(cd "$(dirname "$0")/.." && pwd)"
cli_doc="${repo_root_dir}/docs/cli/committer.md"

PATH="bin:${PATH}"

runHelp() {
  local command="$1"
  $command --help 2>&1 | sed -E 's/[[:space:]]+$//g'
}

# generate_doc - Generate the complete CLI help documentation
generate_doc() {
  cat <<'EOF'
<!---
 SPDX-License-Identifier: Apache-2.0
 File generated by help_docs.sh. DO NOT EDIT.
 Re-run scripts/cli_help_docs.sh generate to update.
 --->

# committer

The `committer` command starts Fabric-X committer services and checks service health.

## Environment Variables

All configuration settings can be overridden using environment variables.
The variable name follows the pattern `SC_<SERVICE>_<KEY>`, where dots and dashes
in the key are replaced with underscores.

For example, to disable logging for the sidecar service:

```
SC_SIDECAR_LOGGING_ENABLED=false
```

Service prefixes: `SIDECAR`, `COORDINATOR`, `VERIFIER`, `VC`, `QUERY`.

Example - start the sidecar with logging disabled:

```
SC_SIDECAR_LOGGING_ENABLED=false committer start sidecar -c config.yaml
```

EOF

  local commands=(
    "committer"
    "committer start"
    "committer start sidecar"
    "committer start coordinator"
    "committer start verifier"
    "committer start vc"
    "committer start query"
    "committer healthcheck"
    "committer healthcheck sidecar"
    "committer healthcheck coordinator"
    "committer healthcheck verifier"
    "committer healthcheck vc"
    "committer healthcheck query"
    "committer version"
  )

  for x in "${commands[@]}"; do
    cat <<EOF

## $x

\`\`\`
$(runHelp "$x")
\`\`\`

EOF
  done
}

case "$1" in
"check")
  if [ -n "$(diff -u <(generate_doc) "${cli_doc}")" ]; then
    echo "The CLI reference documentation is out of date."
    echo "Please run '$0 generate' to update the documentation."
    exit 1
  fi
  ;;

"generate")
  generate_doc >"${cli_doc}"
  ;;

*)
  echo "Please specify check or generate"
  exit 1
  ;;
esac
